 <h1>Guerra dos Mundos - novo Card Game</h1>
    <p>Este card game foi criado por Maickon Rangel e Leornado Mota, o nosso objetivo era criar um novo jogo que pudesse nos oferecer um maior número de possibilidades
    tentanto torna-lo muito mais estratégico do que puramente cartas fortes no baralho.</p>
    <h2>A idéia</h2>
    <p>A idéia de se criar um novo card game foi pela razão de que nós não éramos satisfeitos com as regras dos outros card games, achavamos que 
    eram meio limitadas em algums certos momentos. Apartir daí resolvemos criar o nosso próprio card game.</p>
    <h2>As cartas</h2>
    <p>A principio as cartas foram feitas com imagens retirada da internet, usamos o software Magic Set Editor e adaptamos um modelo de carta de Magic
    para criarmos nossas cartas. Parecido com Magic, as cartas possuem habilidades porém no nosso jogo o jogador deverá buscar a descrição de seus efeitos 
    num manual de efeitos onde são descritos os efeitos de todas as cartas do jogo.</p>
    <h2>Os efeitos</h2>
    <p>Os edeitos presentes nas cartas, retratam habilidades presentes em livros de RPG como o D&D. Muitas das habilidades dependem do nivel da riatura,
    isso significa que uma criatura pode ativar o seu efeito causando um estrago maior caso ele seje encantado com alguma magia que aumente o seu nivel.
    A variedade de efeitos são muitas permitindo muitas conbinações incriveis. </p>
    <h2>As regras</h2>
    <p>Nossas regras tentam ao máximo possível retratar um combate real, as criaturas possuem pontos de vida ao serem evocadas e esses pontos não são recuperados
    no final do turno. O limite de criaturas é estabelecido pela quantidade total do nivel das criaturas, pois assim podemos estabelecer o nivel do deck.
    Os pontos de vida do jogador começam com 40 e eles são gastos para evocar suas criaturas. Mais detalhes entre na página de regras.</p>
    