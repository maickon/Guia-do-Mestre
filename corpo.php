  <h1 align="center">Guia Do Mestre</h1>
  <br />
  <p class="margem"> Sejam bem vindos amigos, este é o lugar ideal que vai lhe auxiliar a mestrar sua campanha preferida de Dungeons & Dragons. </p>
  <p class="margem"> Aqui você vai encontrar ferramentas que permitirão que você crie aleatóriamente um personagem de Dungeons & Dragons, armas mágicas, armaduras mágias e escudos.</p>
  <p class="margem">As fichas geradas poderam se exportadas para um formato de texto, podendo assim ser editada pelo jogador.</p>
  <p class="margem">Para lhe ajudar em suas aventuras temos também um gerador de idéias que vai lhe auxiliar
  quando estiver sem idéa para mestrar uma aventura.</p>
  <p class="margem">Contanmos também com outro gerador interessante, trata-se de um gerador de histórias, com ele seus NPCs deixarão
  de ser uma ficha estatisticamente numérica, pois este gerador concede uma base sólida de uma simples história para seus personagens NPCs.</p>
  <p class="margem">O site não está totalmente completo mais acredito que pelo que ja foi servirá de grande ajuda para os mestres. Existem várias outras coisas que ainda desejo fazer como criar 
  um bestiário onde haverá todo o tipo de monstro para o mestre colocar em sua aventura. Alguns links ainda não estão ativados mais no futuro estarão funcionando.
  </p>
  <p class="margem"><b>IMPORTANTE !</b></p>
  <p class="margem">O conteúdo presente aqui usado como as armas,escudos,armaduras e toda regra em si, são pertencentes ao sistema d20. As imagens presentes são de total autoria de seus criadores. 
  Apenas as coloquei por acha-las interessante.</p>
  <p class="margem">Obrigado pela atenção, Bom divertimento a todos.
  </p>
 