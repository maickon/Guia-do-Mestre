<h1>Guerra dos Mundos, O novo Card Game</h1>

<p><strong>Introdução</strong></p>
<p>Este é um card game que incorpora regras voltadas para um jogo de RPG. 
Quando pensamos em criar um jogo de cartas, estavamos pensando em criar algo que vai além de uma simples carta com ataque e defesa. 
Queriamos colocar detalhes que fizessem a carta se parecer com uma ficha, apartir dai decidimos colocar pontos de vida, tendencia,
 associar suas habilidades a talentos, colocar uma resistencia a magias, assim como testes para a criatura não ser envenenada e tudo mais. 
Com tudo isso, esperamos que este card game se torne um jogo tão estratégico quanto o xadrez 
( sei que jamais será, rsrsr mas é o que eu busco...), 
pois minha preocupação maior é que o jogo não se torne um amotoado de cartas poderosas em seu deck que 
lhe faça ganhar em 3 ou menas rodadas sem haver o verdadeiro espiritos de uma batalha que seria uma douradora gostosa partida de se jogar.
<p>

<p><strong>As Cartas</strong></p>

<p>A principio serão definidas três tipos de cartas onde cada tipo terá inúmeras classificações.
 Os tipos de cartas são divididos em: Cartas de monstro, cartas de magia e cartas de artefato, 
 sendo que as cartas de magia são divididas em dois tipos: Pergaminhos e Grimórios, onde cada um tem sua peculiaridade. 
 Os artefatos também são divididos em duas categorias que são: Artefatos comuns, artefatos mágicos e artefatos únicos.
Bem, vamos a descrição deles:
</p>

<h1>1º  tipo [cartas de monstro]</h1>
<p>Estas cartas são compostas com os seguintes atributos:</p>
<p><strong>Nome</strong><br />
Significa o próprio nome da criatura.</p>
 
<p><strong>Raça</strong><br /> a qual raça a criatura pertence.</p>

<p><strong>Classe</strong><br />
Uma classe indica o que o personagem é.</p>

<p><strong>PVS(x)</strong><br />
É definido por um multiplicador de vida que o jogador vai pagar para invocar a sua criatura. 
Ex: Minha carta tem multiplicador x2, eu posso então gastar 2 de vida para baixar a minha criatura com 4 Pvs ou mais.</p>

<p><strong>Ataque</strong><br /> 
Um valor numérico que representa a pontuação de ataque da criatura. 
O ataque indica o quando de dano um carta é capaz de causar ao seu inimigo, 
quando estamos atacando, o defensor utiliza-se dos seus pontos de defesa para bloquear o ataque. 
A diferença entre o ataque e a defesa resulta no dano causado a criatura atacada.</p>

<p><strong>Defesa</strong><br /> 
Um valor numérico que representa a pontuação de defesa da criatura. 
A defesa indica o quando de dano um carta é capaz de suportar de seu inimigo, 
quando estamos nos defendendo, estamos utilizando dos pontos de defesa para bloquear o ataque.  
A diferença entre o ataque e a defesa resulta no dano causado a criatura defensora.</p>

<p><strong>Magia</strong><br /> <br />
Um valor numérico que representa a pontuação de magia da criatura. 
Bem diferente do ataque, quando usamos os pontos de maiga, 
estamos gastando este ponto para conjurar um magia que normalmente está descrita na carta. 
Uma vez conjurada a magia caso ela cause dano, o defensor pode reduzir o dano com o seu ponto de resistência (Res). 
Existem magias que controlam a mente ou simplesmente matam. Para que estas magias surtem efeito em suas vítimas, 
a vítima deve perder em um teste de resistência qua será descrita mais afrente.  
Resistência: um valor numérico que representa a pontuação de resistência da criatura. Este valor funciona como uma defesa, 
porém apenas para magias. Tanto magias que causem dano quanto magias que controlam ou matam. Quando se trata de dano, a resistência simplemente tenta reduzir o valor do dano a máximo possível da mesmo forma que a defesa reduz o ataque. Quando se trata de testes contra controle ou morte, a vítima deverá rolar um dado que somará a sua resistência para não cair no efeito que lhe atacou. 

<p><strong>Tendencia</strong><br />
A tendencia indica o alinhamento da criatura e elas são divididas da seguinte forma: <br />
<strong>L/B (Leal e Bom)</strong> Esta pessoas caminha nos domínios da honra e peixão.<br />
<strong>L/N (Leal e Neutro)</strong> Você é confiável e honrado, porém não é um fanático.<br />
<strong>L/M (Leal e Mal)</strong> Seu instinto se alinha no caminho da maldade intencional, simples prazer de fazer maldade.<br />
<strong>N/B (Neutro e Bom)</strong> Neste caminho caminho você faz o bem sem se preocupar com a ordem.<br />
<strong>N/N (Neutro)</strong> Aque você age de forma natural sem preconceito nem compulsões.<br />
<strong>N/M (Neutro e Mal)</strong> Este caminho representa o mal puro, sem honra ou variação.<br />
<strong>C/B (Caótico e Bom)</strong> Caminhos dos espiritos livres de bom coração.<br />
<strong>C/N (Caótico e Neutro)</strong> Você nao se apega aos valores da sociedade entre fazer o bem e estar correto, você simplemente faz o que você julga melhor sem se importar com os outros e as regras.<br />
<strong>C/M (Caótico e Mal)</strong> Neste caminho a destruição da beleza da vida e da ordem são seuprazer de vida.<br />
</p>
<p><strong>Efeito</strong><br /> Descreve o efeito da criatura, os efeitos são apresentados na carta como palavras chaves. Suadescrição deve ser lida no manual para consultas. 
Descrição: Pode ser um pequeno trecho da frase preferida do personagem ou simplesmente o que as pessoas ouvem falar por ai sobre ele. Um simples resumo de sua história, emfim descrição abrange essas coisas. 
</p>

