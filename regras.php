<h1>3º  tipo [cartas de artefato]</h1>

<p>Os artefatos são as cartas que vão melhorar as suas criaturas seja por equipamento ou por efeitos mágicos. 
Artefatos únicos só podem ter um no deck, 
artefatos comuns podem ter 3 repetidos. Segue abaixo maiores detalhes sobre os artefatos.</p>

<p><strong>Nome</strong> <br />
Define o nome do artefato.</p>

<p><strong>Tipo</strong> <br />
Define o tipo da carta, neste caso se trata de um artefato.</p>

<p><strong>Raridade</strong> <br /> 
A raridade se divide em 2 tipos: único e comum.</p>

<p><strong>Único</strong> <br />
Estes artefatos são únicos existe apenas único desse tipo de artefato no deck.</p>

<p><strong>Comum</strong> <br />
Se enquadram todas as armas comuns que existem, podem haver apenas 3 cartas repetidas no deck.
Os artefatos também podem ser mágicos, geralmente esses artefatos são bem mais poderoso, mas memso assim eles se enquadram entre único ou comum.
Para baixar uma carta de artefato, o jogador preceisa gastar pontos de magia e forja que serão explicados mais adiante.</p>

<p><strong>Efeito</strong> <br />
Determina o que o artefato faz quando equipado.</p>

<p><strong>Magia</strong> <br />
Determina os pontos de magia que deverão ser gastos para baixar o artefato no campo de batalha.</p>

<p><strong>Forja</strong> <br />
Estes pontos também determinam o quanto será necessário de pontos de forja para que o jogador consiga baixar o seu artefato.
Artefatos mágicos por sua vez, logicamente consumirão pontos de forja e magia para serem baixados. Artefatos não mágicos podem ser baixados apenas com pontos de forja.
Uma vez que o artefato ja esteja em campo ele poderá ser anexado em uma criatura que você deseje e a criatura se beneficiará das vantagens concedidas pelo artefato. 
Um equipamento só pode somente ser equipado em seu turno, o jogador poderá remover o equipamento de uma criatura qualquer e equipar em outra criatura ou simplesmente deixar o equipamento sem ninguém.

<p><strong>Descrição</strong> <br  /> 
Uma breve descrição falando normalmente sobre a história do artefato ou contos sobre ele e etc.</p>

