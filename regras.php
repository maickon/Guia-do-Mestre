<h1>O combate</h1>
 
<p>O contexto do combate é bem simples, trata-se de uma batalha entre dois deuses onde eles 
evocam criaturas místicas nativas de um dos 9 mundos conhecidos. Essa criaturas lutam sobre 
o comanto do seu mestre para atingir um único objetico, o domínio dos mundos. Diante dessa 
situação os deuses são protegidos por suas criaturas evocadas que ao memo tempo que o defende, 
contra ataca para derrotar o seu oponente. Uma batalha termina com o fim dos pontos de vida do 
jogador (o suposto deus neste contexto) que são 40 ou quando seus recursos acabam (Sem cartas para comprar). 
Os decks são compostos de no mínimo 40 cartas e no máximo 60 sendo que pode-se haver apenas 25 criaturas e as 
autras cartas podem ser misturadas entre magias e artefatos sem definição de limite para elas.</p>

<p>Um combate se inicia quando o jogador declara dar inicio a fase de combate, 
nesta fase o jogador definirá com quais criaturas ele pretende atacar.
 Uma vez definido com quais criaturas ele atacará, elas por sua vez devem ficar deitadas de forma que o seu oponente 
 saberá identificar quais criaturas estão atacandao. Como o objetivo do jogo é derrotar o oponente em si e não suas criaturas,
  seus ataque sempre se orientaram ao oponente, porém o próprio oponete tem direito de se defender com as criaturas que tem.
 Obs: o jogador em si sempre vai possuir uma carta mestre que o represente dentro do jogo. Na imagem abaixo explicaremos melhor isso.</p>

