<h1>2º  tipo [cartas de magia]</h1>

<p>Qualquer magia conjurada no jogo vem de um conjurador ou de uma carta de magia,
 toda carta de magia tem um custo de pontos de magia que é necessário para conjura-la.
  Existem cartas de magia que ficam no campo por tempo indeterminado e que suas criaturas podem usufluir de seus recursos.
 Estas cartas são chamadas de grimório. Veja com detalhes cada elemento que compõe uma carta de magia.</p>

<p><strong>Nome</strong> <br  /> 
O mome da carta.</p>

<p><strong>Tipo</strong> <br  />
O tipo pode ser instantânea, Perpétua(encantamento), subexistência, global e feitiço.

<p><strong>Tipo 2</strong> <br  />
Este segunto tipo define se a carta consiste em ser uma magia de <strong>pergaminho</strong> ou de <strong>grimório</strong>. A diferença entre eles é a seguinte:
Cartas de pergaminho consistem apenas em uma magia que após pago os devidos pontos de magia, seu efeito é executado e após isso a carta 
é enviada ao cimitério. Salvo um única execessão, caso o pergaminho seja um encantamento ele não será colocado no cimitério após sua conjuração.
Magias de grimório são bem diferentes, uma vez conjuradas, elas permanecem no campo e qualquer criatura que tenha pontos de magia para pagar 
seu custo de aprendizado. Uma vez pago a criatura poderá conjurar o efeito descrito no grimório naquele turno. Caso o grimório seja destruido 
nenhuma criatura capaz de conjuar as magias do grimório poderá mais executa-las. 

<p><strong>Instantânea</strong> <br  />
Este tipo de magia pode ser lançada no turno do oponente.</p>

<p><strong>Perpétua (encantamento)</strong> <br  />
Este tipo de magia possui a característica de tornar a carta conjurada permanente no campo.</p>

<p><strong>Subexistência</strong> <br  />
A cada um dado número de rodadas o efeito desta magia retornará ao campo. Este número de rodadas é definido por um dado, pode ser (d4,d6,d8).</p>

<p><strong>Global</strong> <br  />
São magias que afetam um determinado número de criaturas. Esse número pode ser entre 1d4 a 1d20.</p>

<p><strong>Feitiço</strong> <br  /> 
Um feitiço é uma magia que tem duração até o final do turno.</p>

<p><strong>Efeito</strong> <br  />
A descrição vai descrever o que a magia é capaz de fazer.
</p>

<p><strong>Lv</strong> <br  />
O nivel da magia informa o quanto poderosa é uma magia, entretanto define também se a criatura é capaz de usa-la. Somente uma criatura que tenha nivel igual ao da magia ou maior será capaz de conjura-la.
</p>

<p><strong>Magia</strong> <br  />
Define a quantidade de pontos de magia necessário para o conjurador poder conjurar a magia descrita na carta.
</p>

<p><strong>Descrição</strong> <br  />
A descrição de informa uma breve história sobre a magia lançada.
</p>

