 a criatura é destruida e enviada ao cimitério.</p>


<p><strong>Usando Magia e Resistância</strong></p>

<p>Quando o ataque é feito com uma magia, a batalha é tratada de forma um pouco diferente. Observe: 
primeiro o conjurador precisa gastar os seus pontos de magia para lançar alguma. Uma vez que a magia seja conjurada, 
ela terá um determinado dano ja especificado que será subtraido da resistência da vítima, dentro de um turno, 
os pontos de resistência não se recuperam ou seja caso um mago lance um bola de fogo que cause 2 de dano a uma resistência 4, 
isso significa que sua resistência naquele turno diminuirá em 2 e só se recuperará no próximo turno. 
Uma magia pode ser conjurada várias vezes no mesmo turno, desde que ele possua pontos de magia suficiente para conjurar novamente a magia desejada.
 As magias podem ser conjurada apenas no seu turno, salvo uma única excessão caso a magia seje instantânea.</p>
 
<p>Os pontos de magia se rucuperam a taxa de 1 pt por turno da mesma forma que os pontos de forja</p>

